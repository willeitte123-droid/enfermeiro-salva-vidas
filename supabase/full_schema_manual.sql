-- Busca ID do usuário pelo e-mail
CREATE OR REPLACE FUNCTION public.get_user_id_by_email(email_input TEXT)
RETURNS UUID
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    user_id_val UUID;
BEGIN
    SELECT id INTO user_id_val FROM auth.users WHERE LOWER(email) = LOWER(email_input) LIMIT 1;
    RETURN user_id_val;
END;
$$;

-- Ranking Global
CREATE OR REPLACE FUNCTION public.get_global_ranking()
RETURNS TABLE (
    user_id UUID,
    first_name TEXT,
    last_name TEXT,
    avatar_url TEXT,
    score BIGINT,
    accuracy INT
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    RETURN QUERY
    WITH user_answers AS (
        SELECT 
            u.user_id,
            COUNT(u.id) AS total_q,
            COUNT(CASE WHEN u.is_correct THEN 1 END) AS correct_q
        FROM public.user_question_answers u
        GROUP BY u.user_id
    ),
    user_sims AS (
        SELECT 
            s.user_id,
            SUM(s.score) AS sim_correct,
            SUM(s.total_questions) AS sim_total
        FROM public.user_simulations s
        GROUP BY s.user_id
    )
    SELECT 
        p.id AS user_id,
        COALESCE(p.first_name, 'Usuário') AS first_name,
        COALESCE(p.last_name, '') AS last_name,
        p.avatar_url,
        COALESCE(ua.correct_q, 0) + COALESCE(us.sim_correct, 0) AS score,
        CASE 
            WHEN (COALESCE(ua.total_q, 0) + COALESCE(us.sim_total, 0)) > 0 
            THEN ROUND(((COALESCE(ua.correct_q, 0) + COALESCE(us.sim_correct, 0))::NUMERIC / (COALESCE(ua.total_q, 0) + COALESCE(us.sim_total, 0))::NUMERIC) * 100)::INT
            ELSE 0 
        END AS accuracy
    FROM public.profiles p
    LEFT JOIN user_answers ua ON p.id = ua.user_id
    LEFT JOIN user_sims us ON p.id = us.user_id
    WHERE (COALESCE(ua.correct_q, 0) + COALESCE(us.sim_correct, 0)) > 0
    ORDER BY score DESC, accuracy DESC
    LIMIT 100;
END;
$$;

