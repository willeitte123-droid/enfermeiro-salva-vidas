-- Incrementar Atividade Diária
CREATE OR REPLACE FUNCTION public.increment_user_activity(seconds_to_add INT)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    INSERT INTO public.daily_activity_time (user_id, activity_date, seconds, updated_at)
    VALUES (auth.uid(), CURRENT_DATE, seconds_to_add, NOW())
    ON CONFLICT (user_id, activity_date)
    DO UPDATE SET 
        seconds = public.daily_activity_time.seconds + EXCLUDED.seconds,
        updated_at = NOW();
END;
$$;

-- Busca de Detalhes dos Usuários (Admin)
CREATE OR REPLACE FUNCTION public.get_all_user_details()
RETURNS TABLE (
    id UUID,
    first_name TEXT,
    last_name TEXT,
    role TEXT,
    status TEXT,
    plan TEXT,
    avatar_url TEXT,
    email TEXT,
    access_expires_at TIMESTAMP WITH TIME ZONE,
    plan_start_date TIMESTAMP WITH TIME ZONE,
    last_ip TEXT,
    location TEXT
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    RETURN QUERY
    SELECT 
        u.id,
        p.first_name,
        p.last_name,
        COALESCE(p.role, 'user') AS role,
        COALESCE(p.status, 'active') AS status,
        COALESCE(p.plan, 'free') AS plan,
        p.avatar_url,
        u.email::TEXT,
        p.access_expires_at,
        p.plan_start_date,
        p.last_ip,
        p.location
    FROM auth.users u
    LEFT JOIN public.profiles p ON u.id = p.id
    ORDER BY u.created_at DESC;
END;
$$;

-- Estatísticas Detalhadas de Desempenho do Usuário
CREATE OR REPLACE FUNCTION public.get_detailed_performance_stats(p_user_id UUID)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
    result JSONB;
BEGIN
    SELECT jsonb_build_object(
        'categories', (
            SELECT COALESCE(jsonb_agg(cat_row), '[]'::jsonb)
            FROM (
                SELECT 
                    q.category,
                    COUNT(a.id) AS total_answered,
                    COUNT(CASE WHEN a.is_correct THEN 1 END) AS total_correct
                FROM public.user_question_answers a
                JOIN public.questions q ON a.question_id = q.id
                WHERE a.user_id = p_user_id
                GROUP BY q.category
            ) cat_row
        ),
        'simulations', (
            SELECT COALESCE(jsonb_agg(sim_row), '[]'::jsonb)
            FROM (
                SELECT score, total_questions, percentage, time_taken_seconds, created_at
                FROM public.user_simulations
                WHERE user_id = p_user_id
                ORDER BY created_at ASC
            ) sim_row
        ),
